#!/usr/bin/env python3
"""build.py — Assembles orca_sync_portable.py from source modules.

Usage:  python build.py
Output: orca_sync_portable.py  (gitignored — contains embedded credentials)

The portable script:
  - Installs its own pip dependencies on first run
  - Works on Windows, Linux, and macOS
  - Stores token.json and orca_sync.log in the user's home config dir
    so the USB drive can be read-only
"""

import json
import os
import re

SRC_DIR = os.path.join(os.path.dirname(__file__), "orca_sync")
CREDS_FILE = os.path.join(SRC_DIR, "credentials.json")
OUTPUT_FILE = os.path.join(os.path.dirname(__file__), "orca_sync_portable.py")

# Inline in dependency order; "main" last
MODULE_ORDER = ["config", "auth", "drive_client", "file_watcher", "sync_engine", "tray", "main"]
LOCAL_MODULES = set(MODULE_ORDER)

_LOCAL_IMPORT_RE = re.compile(
    r"^(?:from|import)\s+(" + "|".join(re.escape(m) for m in LOCAL_MODULES) + r")\b.*$",
    re.MULTILINE,
)
_MAIN_GUARD_RE = re.compile(
    r'\nif __name__\s*==\s*["\']__main__["\']\s*:[ \t]*\n(?:[ \t]+.*\n?)*'
)

# ── Bootstrap code (pure stdlib — runs before any third-party imports) ────────

_BOOTSTRAP = """\
import subprocess as _subprocess, sys as _sys, importlib as _importlib

_REQUIRED = [
    ("watchdog",                 "watchdog"),
    ("google-auth",              "google.auth"),
    ("google-auth-oauthlib",     "google_auth_oauthlib"),
    ("google-auth-httplib2",     "google_auth_httplib2"),
    ("google-api-python-client", "googleapiclient"),
    ("pystray",                  "pystray"),
    ("Pillow",                   "PIL"),
]


def _can_import(name):
    try:
        _importlib.import_module(name)
        return True
    except ImportError:
        return False


_missing = [pkg for pkg, imp in _REQUIRED if not _can_import(imp)]
if _missing:
    print(f"OrcaSync: installing {len(_missing)} package(s): {', '.join(_missing)}")
    _subprocess.check_call([_sys.executable, "-m", "pip", "install", "--user"] + _missing)
    print("Packages installed. Starting...")
"""

# ── Portable path overrides (injected after config.py section) ────────────────

_PORTABLE_OVERRIDES = """\
# ── Portable overrides: store token/log in user home, not on USB ─────────────
import platform as _platform, tempfile as _tempfile, atexit as _atexit


def _user_data_dir():
    if _platform.system() == "Windows":
        return os.path.join(os.environ.get("APPDATA", os.path.expanduser("~")), "orca_sync")
    return os.path.join(os.path.expanduser("~"), ".config", "orca_sync")


_DATA_DIR = _user_data_dir()
os.makedirs(_DATA_DIR, exist_ok=True)
TOKEN_FILE = os.path.join(_DATA_DIR, "token.json")
LOG_FILE = os.path.join(_DATA_DIR, "orca_sync.log")

# Write embedded credentials to a temp file so the auth flow can read them
_creds_fd, CREDENTIALS_FILE = _tempfile.mkstemp(suffix=".json")
with os.fdopen(_creds_fd, "w") as _f:
    _f.write(_CREDENTIALS_JSON)
_atexit.register(os.unlink, CREDENTIALS_FILE)
"""


def _read_module(name: str) -> str:
    with open(os.path.join(SRC_DIR, f"{name}.py"), encoding="utf-8") as f:
        return f.read()


def _process(source: str) -> str:
    source = _LOCAL_IMPORT_RE.sub("", source)
    source = _MAIN_GUARD_RE.sub("\n", source)
    return source.strip()


def main():
    if not os.path.isfile(CREDS_FILE):
        raise FileNotFoundError(
            f"credentials.json not found at {CREDS_FILE}\n"
            "Download it from Google Cloud Console > APIs & Services > Credentials."
        )

    with open(CREDS_FILE, encoding="utf-8") as f:
        creds_raw = f.read().strip()
    json.loads(creds_raw)  # validate

    parts = []

    parts.append('''\
#!/usr/bin/env python3
"""OrcaSlicer Profile Sync — Portable Edition

Single-file, self-installing. Runs on Windows / Linux / macOS.
Requires Python 3.8+. Dependencies are installed automatically on first run.
Token and logs are stored in your user config directory; this file can live
on a read-only USB drive.

DO NOT share this file — it contains embedded Google OAuth credentials.
"""
''')

    parts.append("# ── Bootstrap ────────────────────────────────────────────────────────────\n")
    parts.append(_BOOTSTRAP)
    parts.append("\n")

    parts.append("# ── Embedded credentials (generated by build.py — do not share) ─────────\n")
    parts.append(f"_CREDENTIALS_JSON = {repr(creds_raw)}\n\n")

    for mod in MODULE_ORDER:
        source = _process(_read_module(mod))
        divider = "═" * 70
        parts.append(f"# {divider}\n# {mod}.py\n# {divider}\n\n")
        parts.append(source)
        parts.append("\n\n")

        if mod == "config":
            parts.append(_PORTABLE_OVERRIDES)
            parts.append("\n")

    parts.append('if __name__ == "__main__":\n    main()\n')

    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        f.write("".join(parts))

    print(f"Built: {OUTPUT_FILE}")
    print("WARNING: This file contains embedded credentials — keep it private, do not commit it.")


if __name__ == "__main__":
    main()
